x=3
y=9

#arithmetic operators
print(x+y) #add
print(x-y) #minus
print(x*y) #multiply
print(x/y) #divide
print(x%y) #modulus (remainder)
print(x**y) #exponentiation (power)
x = 9.191823
print(x//y) #floor division (gives largest integer possible; neglects decimals)
#assignment operators
x = 9 		#let x=9
x += 3 		#x=x+3
print(x)
x = 9
x -=3 		#x=x-3
print(x)
x *= 3 		#x=x*3
print(x)
x /= 3 		#x=x/3
print(x)
x **= 3 	#x=x**3
print(x)

#comparison operators
x = 9
y = 3
print(x==y) 		#"true" if x=y, otherwise "false"
print(x!=y) 		#"true" if x doesn't equal y, otherwise "false"
print(x>y) 			#"true" if x>y, otherwise "false"
print(x<y) 			#"true" if x<y, otherwise "false"
print(x>=y) 		#"true" if x is greater than or equal to y, otherwise "false"
print(x<=y) 		#"true" if x is less than or equal to y, otherwise "false"